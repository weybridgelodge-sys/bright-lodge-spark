#!/usr/bin/env node
/**
 * Import real member testimonials from a CSV file into src/data/testimonials.ts.
 *
 * Usage:
 *   node scripts/import-testimonials.mjs scripts/testimonials.csv
 *
 * CSV columns (header row required):
 *   quote,name,role,joined
 *
 * - Quote values may contain commas and newlines if wrapped in double quotes.
 * - Escape an internal double quote by doubling it ("" inside a quoted field).
 *
 * The script rewrites src/data/testimonials.ts atomically.
 * Run `bun run dev` (or refresh the preview) to see the new content.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const inputPath = process.argv[2];
if (!inputPath) {
  console.error("Usage: node scripts/import-testimonials.mjs <path-to-csv>");
  process.exit(1);
}

const csv = readFileSync(resolve(inputPath), "utf8");

// Minimal RFC-4180-ish CSV parser (handles quoted fields, commas, newlines).
function parseCSV(text) {
  const rows = [];
  let row = [];
  let field = "";
  let inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"' && text[i + 1] === '"') { field += '"'; i++; }
      else if (c === '"') { inQuotes = false; }
      else { field += c; }
    } else {
      if (c === '"') inQuotes = true;
      else if (c === ",") { row.push(field); field = ""; }
      else if (c === "\n" || c === "\r") {
        if (c === "\r" && text[i + 1] === "\n") i++;
        row.push(field); rows.push(row); row = []; field = "";
      } else field += c;
    }
  }
  if (field.length || row.length) { row.push(field); rows.push(row); }
  return rows.filter((r) => r.length && r.some((x) => x !== ""));
}

const rows = parseCSV(csv);
const [header, ...data] = rows;
const cols = header.map((h) => h.trim().toLowerCase());
const idx = {
  quote: cols.indexOf("quote"),
  name: cols.indexOf("name"),
  role: cols.indexOf("role"),
  joined: cols.indexOf("joined"),
};
for (const [key, i] of Object.entries(idx)) {
  if (i < 0) { console.error(`Missing CSV column: ${key}`); process.exit(1); }
}

const items = data.map((r) => ({
  quote: r[idx.quote]?.trim() || "",
  name: r[idx.name]?.trim() || "",
  role: r[idx.role]?.trim() || "",
  joined: r[idx.joined]?.trim() || "",
})).filter((t) => t.quote && t.name);

if (!items.length) { console.error("No testimonials parsed."); process.exit(1); }

const escape = (s) => s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");

const fileBody = `// AUTO-GENERATED by scripts/import-testimonials.mjs — ${new Date().toISOString()}
// Edit the source CSV and re-run the script, or edit this file directly.

export interface Testimonial {
  quote: string;
  name: string;
  role: string;
  joined: string;
}

export const testimonials: Testimonial[] = [
${items
  .map(
    (t) => `  {
    quote: \`${escape(t.quote)}\`,
    name: ${JSON.stringify(t.name)},
    role: ${JSON.stringify(t.role)},
    joined: ${JSON.stringify(t.joined)},
  },`
  )
  .join("\n")}
];
`;

const outPath = resolve(__dirname, "../src/data/testimonials.ts");
writeFileSync(outPath, fileBody, "utf8");
console.log(`✔ Wrote ${items.length} testimonials to ${outPath}`);
